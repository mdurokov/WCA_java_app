    private void fillFrame(Competition entity){
        nameLbl.setText(entity.getName());
        startDateLbl.setText(sdf.format(entity.getStartDate()));
        endDateLbl.setText(sdf.format(entity.getEndDate()));
        delegatelbl.setText(entity.getWcaDelegate());
        organiserLbl.setText(entity.getOrganiser());
        informationTxt.setText(entity.getInformation());
        specificationTxt.setText(entity.getSpecification());
        countryLbl.setText(entity.getCountry_id().getName() + ", " 
                + entity.getCountry_id().getContinent_id().getName());
        cityLbl.setText(entity.getCity());
        venueLbl.setText(entity.getVenue());
        venueDetailsLbl.setText(entity.getVenueDetails());
        venueAddressLbl.setText(entity.getVenueAddress());
        if (!("null".equals(String.valueOf(entity.getLongitude())))) {
        longitudeLbl.setText(entity.getLongitude().toString());
        }else{
            longitudeLbl.setText("");
        }
        if(!("null".equals(String.valueOf(entity.getLatitude())))){
            latitudeLbl.setText(entity.getLatitude().toString());
        } else {
            latitudeLbl.setText("");
        }
    }
    
    
    
    


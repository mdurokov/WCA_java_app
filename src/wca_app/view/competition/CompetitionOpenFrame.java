    protected void refreshEntityView(Competition entity) {
        try {
            List<Result> results = controller
                    .getEntities(entity);
            CompetitorsFromCompetitonTableModel model
                    = new CompetitorsFromCompetitonTableModel(results);
            table.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error refreshing competition "
                    + "view: " + e, "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
